using UnityEngine;

// ??????????????????????????????????????????????????????????????????????????????
//  HideUntilGameStart
//  Add this to your player prefab root instead of disabling the whole object.
//  Hides all renderers and disables physics so the player is invisible and
//  frozen during the lobby, while keeping PlayerInput alive and paired.
//  MultiplayerJoinManager.BeginGame() calls Show() when the game starts.
// ??????????????????????????????????????????????????????????????????????????????
public class HideUntilGameStart : MonoBehaviour
{
    private Renderer[] _renderers;
    private CharacterController _controller;

    private void Awake()
    {
        _renderers = GetComponentsInChildren<Renderer>();
        _controller = GetComponent<CharacterController>();
        Hide();
    }

    public void Hide()
    {
        foreach (var r in _renderers) r.enabled = false;
        if (_controller != null) _controller.enabled = false;
    }

    public void Show()
    {
        foreach (var r in _renderers) r.enabled = true;
        if (_controller != null) _controller.enabled = true;
    }
}